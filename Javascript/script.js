function suma() {
    let n1 = parseFloat(prompt("Dime un num"));
    let n2 = parseFloat(prompt("Dime otro num"));
    document.write(n1+n2)
}
function resta() {
    let n1 = parseFloat(prompt("Dime un num"));
    let n2 = parseFloat(prompt("Dime otro num"));
    document.write(n1-n2)
}
function multiplicacion() {
    let n1 = parseFloat(prompt("Dime un num"));
    let n2 = parseFloat(prompt("Dime otro num"));
    document.write(n1*n2)
}
function division() {
    let n1 = parseFloat(prompt("Dime un num"));
    let n2 = parseFloat(prompt("Dime otro num"));
    document.write(n1/n2)
}

function arrel() {
    let n1 = parseFloat(prompt("Dime un num"));
    document.write(Math.sqrt(n1))
}